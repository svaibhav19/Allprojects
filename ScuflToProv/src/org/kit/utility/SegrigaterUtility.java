package org.kit.utility;

import java.util.ArrayList;

import org.kit.moml.api.Entity;
import org.kit.moml.api.Link;
import org.kit.moml.api.Port;
import org.kit.moml.api.Relation;

public class SegrigaterUtility {

	/*
	 * private HashMap<String, Entity> W = new HashMap<String, Entity>();
	 * private HashMap<String, Property> S = new HashMap<String, Property>();
	 * private HashMap<String, Relation> R = new HashMap<String, Relation>();
	 */
	private ArrayList<Entity> W = new ArrayList<Entity>();
	private ArrayList<Port> S = new ArrayList<Port>();
	private ArrayList<Relation> R = new ArrayList<Relation>();
	private ArrayList<Link> L = new ArrayList<Link>();

	public void segrigate(Entity momlEntity) {
		for (Object obj : momlEntity.getClazzOrConfigureOrDeleteEntity()) {

			if (obj.getClass().equals(Port.class)) {
				// Property property = (Property) obj;
				// S.put(property.getName(), property);
				S.add((Port) obj);
			} else if (obj.getClass().equals(Entity.class)) {
				// Entity enty = (Entity) obj;
				// W.put(enty.getName(), enty);
				W.add((Entity) obj);
			} else if (obj.getClass().equals(Relation.class)) {
				// Relation relation = (Relation) obj;
				// R.put(relation.getName(), relation);
				R.add((Relation) obj);
			} else if (obj.getClass().equals(Link.class)) {
				// Link link = (Link) obj;
				// L.add(link);
				L.add((Link) obj);
			}
		}
	}

	public ArrayList<Entity> getW() {
		return W;
	}

	public void setW(ArrayList<Entity> w) {
		W = w;
	}

	public ArrayList<Port> getS() {
		return S;
	}

	public void setS(ArrayList<Port> s) {
		S = s;
	}

	public ArrayList<Relation> getR() {
		return R;
	}

	public void setR(ArrayList<Relation> r) {
		R = r;
	}

	public ArrayList<Link> getL() {
		return L;
	}

	public void setL(ArrayList<Link> l) {
		L = l;
	}

	public ArrayList<Port> segrigatePorts(Entity momlEntity) {
		ArrayList<Port> portList = new ArrayList<Port>();
		for (Object obj : momlEntity.getClazzOrConfigureOrDeleteEntity()) {

			if (obj.getClass().equals(Port.class)) {
				// Property property = (Property) obj;
				// S.put(property.getName(), property);
				portList.add((Port) obj);
			}
		}
		return portList;

	}
}
